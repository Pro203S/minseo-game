const database = new DatabaseSync(path.join(process.cwd(), "database.db"));
const rows = database.prepare("SELECT id, tags FROM games").all();
const update = database.prepare("UPDATE games SET tags = ? WHERE id = ?");
const untranslated = new Set();
const translatedRows = [];

for (const row of rows) {
    const tags = JSON.parse(row.tags);
    const translated = tags.map((tag) => {
        const normalized = String(tag).trim();
        const result = translations[tag] ?? translations[normalized] ?? normalized;
        if (/[A-Za-z]/.test(result)) untranslated.add(result);
        return result;
    });
    translatedRows.push([JSON.stringify([...new Set(translated)]), row.id]);
}

if (untranslated.size > 0) {
    database.close();
    throw new Error(`미번역 태그: ${[...untranslated].sort().join(", ")}`);
}

database.exec("BEGIN IMMEDIATE");
try {
    for (const parameters of translatedRows) update.run(...parameters);
    database.exec("COMMIT");
} catch (error) {
    database.exec("ROLLBACK");
    throw error;
} finally {
    database.close();
}

console.log(`${translatedRows.length.toLocaleString()}개 게임의 태그를 한글화했습니다.`);
